function sum(a,b){
    return a + b
}

function substract(a,b) {
    return a - b
}

function mult(a,b) {
    return a * b
}

function divide(a,b) {
    return a / b
}

function sqrt(a) {
    return  Math.floor(Math.sqrt(a) * 10) / 10
}

function percent(target,percent) {
    return (target/100) * percent
}